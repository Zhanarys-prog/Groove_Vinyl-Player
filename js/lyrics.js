// ── Init ─────────────────────────────────────────────────────────────────────

document.addEventListener('DOMContentLoaded', () => {

// ── State ─────────────────────────────────────────────────────────────────────

let lyricsOpen   = false;
let currentLines = [];
let lyricsTimer  = null;
let lastActiveIdx = -1;

// ── DOM ───────────────────────────────────────────────────────────────────────

const lyricsOverlay   = document.getElementById('lyricsOverlay');
const lyricsBody      = document.getElementById('lyricsBody');
const lyricsSongTitle = document.getElementById('lyricsSongTitle');
const lyricsBtn       = document.getElementById('lyricsBtn');
const lyricsClose     = document.getElementById('lyricsClose');
const audioEl         = document.getElementById('audioPlayer');

// ── Open / Close ──────────────────────────────────────────────────────────────

function openLyrics(track) {
  if (!lyricsOverlay) return;
  lyricsOpen = true;
  lyricsOverlay.classList.add('open');
  if (lyricsBtn) lyricsBtn.classList.add('active');
  loadLyrics(track);
}

function closeLyrics() {
  if (!lyricsOverlay) return;
  lyricsOpen = false;
  lyricsOverlay.classList.remove('open');
  if (lyricsBtn) lyricsBtn.classList.remove('active');
  if (lyricsTimer) { cancelAnimationFrame(lyricsTimer); lyricsTimer = null; }
}

lyricsBtn?.addEventListener('click', () => {
  if (lyricsOpen) {
    closeLyrics();
  } else {
    openLyrics(window.state?.current || null);
  }
});

lyricsClose?.addEventListener('click', closeLyrics);

document.addEventListener('keydown', e => {
  if (e.key === 'Escape' && lyricsOpen) closeLyrics();
});

// ── Load lyrics ───────────────────────────────────────────────────────────────

function loadLyrics(track) {
  if (!lyricsBody) return;
  const title = track?.title || '';
  if (lyricsSongTitle) lyricsSongTitle.textContent = title;

  const key = Object.keys(LYRICS_DB).find(k =>
    title.toLowerCase().includes(k.toLowerCase()) ||
    k.toLowerCase().includes(title.toLowerCase())
  );

  if (!key) {
    lyricsBody.innerHTML = '<p class="lyrics-empty">Нет текста для этого трека</p>';
    currentLines = [];
    return;
  }

  currentLines = LYRICS_DB[key];
  renderLines();
  startSync();
}

// ── Render lines ──────────────────────────────────────────────────────────────

function renderLines() {
  lyricsBody.innerHTML = '';
  currentLines.forEach((line, i) => {
    if (!line[1]) return;
    const p = document.createElement('p');
    p.className   = 'lyrics-line';
    p.textContent = line[1];
    p.dataset.idx = i;
    lyricsBody.appendChild(p);
  });
}

// ── Sync ──────────────────────────────────────────────────────────────────────

function getCurrentTime() {
  // Use real audio time if playing, otherwise use demo timer
  const audio = audioEl;
  if (audio && audio.src && audio.duration && !isNaN(audio.currentTime) && audio.currentTime > 0) {
    return audio.currentTime;
  }
  // Fall back to demo time from state
  return window.state?.demoTime || 0;
}

function syncLyrics() {
  if (!lyricsOpen || !currentLines.length) {
    lyricsTimer = requestAnimationFrame(syncLyrics);
    return;
  }

  const time = getCurrentTime();

  let activeIdx = -1;
  for (let i = currentLines.length - 1; i >= 0; i--) {
    if (time >= currentLines[i][0]) { activeIdx = i; break; }
  }

  if (activeIdx !== lastActiveIdx) {
    lastActiveIdx = activeIdx;
    const allLines = lyricsBody.querySelectorAll('.lyrics-line');
    let activeEl = null;

    allLines.forEach(el => {
      const idx = parseInt(el.dataset.idx);
      el.classList.remove('active', 'past');
      if (idx === activeIdx) {
        el.classList.add('active');
        activeEl = el;
      } else if (currentLines[idx] && activeIdx >= 0 && currentLines[idx][0] < currentLines[activeIdx][0]) {
        el.classList.add('past');
      }
    });

    if (activeEl) activeEl.scrollIntoView({ behavior: 'smooth', block: 'center' });
  }

  lyricsTimer = requestAnimationFrame(syncLyrics);
}

function startSync() {
  if (lyricsTimer) cancelAnimationFrame(lyricsTimer);
  lastActiveIdx = -1;
  lyricsTimer = requestAnimationFrame(syncLyrics);
}

// ── Hook into track changes ───────────────────────────────────────────────────

window.onLyricsTrackChange = function(track) {
  if (lyricsOpen) loadLyrics(track);
};

}); // end DOMContentLoaded
